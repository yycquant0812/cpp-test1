/* ========================================
   函式範例 - 新台幣轉美元
   ========================================
   程式功能：
   將新台幣轉換為美元

   使用固定匯率：1 美元 = 31 新台幣

   教學重點：
   1. 函式的定義與呼叫
   2. 參數傳遞 (Pass by Value)
   3. 回傳值的使用
   4. float 浮點數資料型別

   函式結構：
   - 回傳型別：float
   - 函式名稱：dollar
   - 參數：float m (新台幣金額)
   - 回傳值：美元金額
   ======================================== */

#include<iostream>
using namespace std;

/* ========================================
   函式名稱：dollar
   功能：將新台幣轉換為美元
   參數：m - 新台幣金額 (float)
   回傳值：美元金額 (float)

   計算公式：
   美元 = 新台幣 ÷ 匯率
   美元 = m ÷ 31

   範例：
   輸入：310 新台幣
   計算：310 ÷ 31 = 10
   輸出：10 美元

   函式執行流程：
   1. 接收參數 m (新台幣金額)
   2. 將 m 除以匯率 31，結果存入變數 d
   3. 回傳 d 的值給呼叫者

   注意事項：
   - 使用 float 型別以支援小數點運算
   - 匯率設定為固定值 31 (教學簡化)
   - 實際應用中，匯率應該是變動的參數
   ======================================== */
float dollar (float m)
{
    // 計算美元金額 = 新台幣 ÷ 匯率
    float d = m / 31;

    // 回傳計算結果
    return d;
}

/* ========================================
   主程式
   ======================================== */
int main()
{
   float x;

   // 提示使用者輸入新台幣金額
   cout <<"要兌換多少新台幣呢？";
   cin >> x;

   // 呼叫 dollar 函式進行轉換，並輸出結果
   // x 作為參數傳入，dollar(x) 回傳美元金額
   cout << x <<"新台幣可以兌換" << dollar(x) << "美元";

   return 0;
}

/* ========================================
   程式執行範例：

   輸入：要兌換多少新台幣呢？620
   輸出：620新台幣可以兌換20美元

   執行過程分析：
   1. 使用者輸入 620，存入變數 x
   2. 呼叫 dollar(620)
   3. 函式內部計算：d = 620 / 31 = 20
   4. 函式回傳 20
   5. 輸出：620新台幣可以兌換20美元

   教學討論點：
   Q1: 為什麼使用 float 而不是 int？
   A1: 因為匯率轉換通常會產生小數，例如：
       100 新台幣 = 100/31 = 3.225806... 美元

   Q2: 如果要支援多種貨幣轉換怎麼辦？
   A2: 可以增加匯率參數，例如：
       float convert(float amount, float rate)

   Q3: 這個函式可以改進嗎？
   A3: 可以改進的地方：
       - 將匯率設為參數而非固定值
       - 加入輸入驗證 (金額不能為負數)
       - 控制輸出小數位數
   ======================================== */
