/* ========================================
   費氏數列 (Fibonacci Sequence) - 遞迴實作
   ========================================
   費氏數列定義：
   f(1) = 1
   f(2) = 1
   f(n) = f(n-1) + f(n-2), 當 n > 2

   數列：1, 1, 2, 3, 5, 8, 13, 21, 34...
   ======================================== */

#include<iostream>
using namespace std;

// 函式原型宣告：計算第 n 項費氏數
int f(int);

int main(void)
{
    int n;

    // 提示使用者輸入要計算第幾項
    cout<<"Input n:";
    cin>>n;

    // 呼叫遞迴函式計算並輸出結果
    cout<<"f("<<n<<")="<<f(n)<<endl;

    system("pause");
}

/* ========================================
   函式名稱：f
   功能：使用遞迴方式計算費氏數列的第 n 項
   參數：n - 要計算的項數
   回傳值：第 n 項的費氏數

   遞迴原理說明：
   1. 基底條件 (Base Case)：
      當 n=1 或 n=2 時，直接回傳 1
      這是遞迴的終止條件

   2. 遞迴條件 (Recursive Case)：
      f(n) = f(n-1) + f(n-2)
      將問題分解為兩個較小的子問題

   範例：計算 f(5)
   f(5) = f(4) + f(3)
        = [f(3) + f(2)] + [f(2) + f(1)]
        = [[f(2) + f(1)] + 1] + [1 + 1]
        = [[1 + 1] + 1] + 2
        = [2 + 1] + 2
        = 3 + 2
        = 5
   ======================================== */
int f(int n)
{
  // 基底條件：第1項和第2項都是1
  if(n==1 || n==2)
    return 1;

  // 遞迴條件：當前項 = 前一項 + 前兩項
  return f(n-1) + f(n-2);
}

